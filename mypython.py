def addNums(a, b):
    return a + b


def multiplyNums(a, b):
    return a * b


def subtractNums(a, b):
    return a - b


def divideNums(a, b):
    return a / b


def remainderNums(a, b):
    return a % b


def negateNums(a):
    return -a


def squareNums(a):
    return a * a


def cubeNums(a):
    return a * a * a


def main():
    print(addNums(5, 4))
    print(multiplyNums(5, 4))
    print(subtractNums(5, 4))
    print(divideNums(5, 4))
    print(remainderNums(5, 4))
    print(negateNums(5))
    print(squareNums(5))
    print(cubeNums(5))
